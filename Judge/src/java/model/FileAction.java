    private static char removeAccent(char ch) {
        int index = Arrays.binarySearch(SOURCE_CHARACTERS, ch);
        if (index >= 0) {
            ch = DESTINATION_CHARACTERS[index];
        }
        return ch;
    }

    private static String removeAccent(String str) {
        StringBuilder sb = new StringBuilder(str);
        for (int i = 0; i < sb.length(); i++) {
            sb.setCharAt(i, removeAccent(sb.charAt(i)));
        }
        return sb.toString();
    }
    
    public static String deleteFile(String img, String realPath) {
        String name = realPath + img;
        name = realPath(name);
        try {
            File file = new File(name);
            if (file.delete()) {
                return "Deleted";
            } else {
                return "Can't Delete";
            }
        } catch (Exception e) {
            return "Fail";
        }
    }
    
    public static String File(Part part, String realPath, String name, int year) {
        try {
            String filename = Paths.get(part.getSubmittedFileName()).getFileName().toString();
            realPath = realPath + "images";
            if (!Files.exists(Paths.get(realPath))) {
                Files.createDirectory(Paths.get(realPath));
            }
            realPath = realPath(realPath);
            part.write(realPath + "/" + filename);
            String[] partName = filename.split("\\.");
            String newfilename = name.replaceAll("\\s", "") + year
                    + "." + partName[partName.length - 1];
            newfilename = Check(newfilename);
            File oldfile = new File(realPath + "/" + filename);
            File newfile = new File(realPath + "/" + newfilename);
            oldfile.renameTo(newfile);
            return newfilename;
        } catch (IOException e) {
            return null;
        }
    }
    
